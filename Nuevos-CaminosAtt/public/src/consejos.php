                    <br><br>Comprobante de domicilio a su nombre o el de un familiar cercano (cónyuge, pareja, padres, hermanos e hijos). En estos casos, presentar también documento original y copia que acredite su parentesco o estado civil (DNI, acta de matrimonio o unión estable, acta de nacimiento) – original y copia.
