                  <h3>SUS</h3>
                  <h6 style="color: #505050;">
                    El "Sistema Único de Saúde" (SUS) es uno de los sistemas públicos de salud más grandes y complejos del mundo, que abarca diversos servicios médicos y garantiza el acceso pleno, universal y gratuito para toda la población brasileña.

                    Cualquier persona en territorio brasileño tiene derecho al SUS, desde el embarazo y durante toda la vida, con enfoque de salud con calidad de vida, con vistas a la prevención y promoción de la salud.

                    <br><br>
                    <img src="imagesconse/sus.jpg" width="400" height="400"><br><br>
                    "Cartão nacional de Saúde" es el documento de identificación del usuario del SUS. Este registro contiene información sobre personas físicas, tales como:
                    <br><br>Nombre, nombre de la madre, fecha de nacimiento.
                    <br><br>Números de teléfono, dirección, correos electrónicos.
                    <br><br>RG, CPF, Certificados.
                    <br><br>Actualmente, el número CNS está incluido en sistemas informatizados de salud que requieren la identificación de las personas, ya sean usuarios, operadores o profesionales de la salud. De esta manera, el CNS permite crear el historial de atención de cada ciudadano en el Sistema Único de Salud (SUS).
                    <br><br><h4>¿Cómo obtener una tarjeta SUS?</h4>
                    <h6 style="color: #505050;">
                    Simplemente acuda a la "Unidade básica de Saúde" (UBS) más cercana a su domicilio y solicite la inclusión del sistema. Deberá traer un documento de identificación (RG, CPF) y proporcionar su dirección completa.<br><br>
                    </h6>

                    <h3>SUS y el plan de salud privado</h3><br>

                    <h4>SUS</h4><br>
                    <h6 style="color: #505050;">
                    Totalmente financiado con recursos públicos, no hay costos directos para los usuarios por los servicios utilizados.
                    <br><br>Pueden tener tiempos de espera más largos para citas, exámenes y procedimientos, debido a la alta demanda y recursos limitados.
                    <br><br>La calidad puede variar significativamente según la ubicación y la disponibilidad de recursos. Las instalaciones y el equipo pueden ser más antiguos o limitados en comparación con los hospitales privados.
                    <br><br>Los pacientes generalmente tienen menos opciones sobre los médicos u hospitales donde reciben atención, y están sujetos a una asignación basada en la región y la disponibilidad.
                    <br><br>Proporciona acceso a una lista de medicamentos esenciales de forma gratuita.
                    </h6>
                    <br><br><h4>Plan de salud privado</h4><br>
                    <h6 style="color: #505050;"><h6 style="color: #505050;">
                    Los usuarios pagan tarifas mensuales que varían según el plan elegido, el grupo de edad del beneficiario y el tipo de cobertura deseada.
                    <br><br>Generalmente ofrecen un acceso más rápido a citas y trámites.
                    <br><br>Suelen ofrecer mejor infraestructura y comodidad, siendo los hospitales y clínicas generalmente más modernos y mejor equipados.
                    <br><br>Ofrecen mayor libertad para elegir médicos, especialistas y hospitales.
                    <br><br>Dependiendo del plan, puede haber una mayor disponibilidad de medicamentos, incluidas opciones más nuevas o de mayor costo.
                    </h6>
