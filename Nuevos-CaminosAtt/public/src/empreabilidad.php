                  <h3>Cartera de Trabajo</h3>
                  <img src="imagesempre/clt.jpg" width="700" height="700" id="imgbody"> 
                  <h6 style="color: #505050;" id="3"> <br>
                    La "Carteira de Trabalho e Previdência Social" (CTPS) es un documento para que los trabajadores trabajen regularmente en el mercado laboral brasileño.

                    <br><br>Lo proporciona el Ministerio de Trabajo y Empleo y tiene como objetivo registrar la trayectoria profesional del trabajador, incluidos sus derechos laborales.

                    <br><br>La tarjeta de trabajo acredita el tiempo trabajado, recibiendo beneficios, además de ser importante para la jubilación.

                    <br><br><h4>Principales datos registrados en la tarjeta de trabajo:</h4>
                    <br><h6 style="color: #505050;">nombre, fecha de nacimiento, CPF, firma;
                    <br><br>constancia de contrato, salario, función;
                    <br><br>fecha de admisión y despido;
                    <br><br>Vacaciones, accidentes de trabajo y cambios salariales.</h6>

                    <br><br><h4>Documentos necesarios para obtener un permiso de trabajo</h4>
                    <br><h6 style="color: #505050;">CPF;
                    <br><br>Foto reciente y colorida de 3x4;
                    <br><br>Documento de identificación oficial con fotografía;
                    <br><br>Comprobante de residencia con código postal;
                    <br><br>Certificado de nacimiento o matrimonio;
                    <br><br>Los inmigrantes también deberán incluir su Tarjeta de Registro Nacional de Migración <strong>(CRNM)</strong>, la cual se explicará más adelante en este sitio web.</h6>

                    <br><br><h4>Dónde conseguir la tarjeta de trabajo?</h4><br>
                    <h6 style="color: #505050;">
                    La Tarjeta de Trabajo física se puede retirar en estaciones de servicio autorizadas, como unidades Poupatempo (en São Paulo), UAI (en Minas Gerais). Este servicio es gratuito para los ciudadanos.
                    </h6>
                  </h6>
