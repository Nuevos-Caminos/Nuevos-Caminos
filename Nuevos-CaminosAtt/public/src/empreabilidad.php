                  <h3>CRNM y RNM</h3>
                  <img src="imagesempre/CRNM.png" width="700" height="700" id="imgbody">
                  <h6 style="color: #505050;"><br>
                    "Carteira de Registro Nacional Migratório" (CRNM) es el documento de identificación de inmigrantes registrados en Brasil, válido en todo el territorio nacional. 
                    "Registro Nacional Migratório" (RNM) es el número de registro que se encuentra en el CRNM, resultante de la identificación de cada inmigrante a través de su <strong id="4">información personal.</strong>

                    <br><br>Los inmigrantes con <strong>visa temporal</strong> deberán solicitar el registro dentro de los <strong>90 días</strong>, contados a partir de la fecha de ingreso al territorio nacional.

                    El inmigrante a quien se le haya concedido una solicitud de permiso de residencia en el país deberá solicitar el registro dentro de los <strong>30 días</strong>, contados a partir de la fecha de publicación de la aprobación de dicha solicitud.

                    <br><br><h4>Documentación necesaria para registrarse</h4>
                    <br><h6 style="color: #505050;">Documento que acredite identidad y nacionalidad;
                    <br><br>Comprobante de pago de la tasa de emisión del CRNM <strong>(valor R$ 204,77);</strong>
                    <br><br>Si no se encuentra disponible el sistema de recolección de datos biométricos de la Policía Federal, podrá solicitar 1 fotografía reciente de 3x4, a color, fondo blanco, papel normal, de frente.</h6>
                    <br><br><h4>Donde hacer el CRNM</h4>
                    <h6 style="color: #505050;">
                    El solicitante deberá acudir a cualquier unidad de la Policía Federal que brinde asistencia a inmigrantes.
                    </h6>
                  </h6>
                </li>
                <li>
                  <h3>Salario mínimo</h3>
                  <h6 style="color: #505050;">
                    El salario mínimo pretende ser un recurso mínimo para que el trabajador sobreviva. Esto significa que el salario mínimo debe ser suficiente para cubrir la alimentación, la vivienda, el vestido, la higiene y el transporte. Este valor actúa como mantenedor del poder adquisitivo de la población.

                    <br><br>A partir de 2020, el salario mínimo se ajusta en función de la inflación, según la constitución, que establece que los ciudadanos no pueden perder poder adquisitivo en función del valor del salario mínimo.

                    <br><br><h4>Valor del salario mínimo</h4>
                    <h6 style="color: #505050;">
                    En 2024, se ajustó a <strong>R$ 1.412,00</strong> y está vigente desde el 1 de enero de 2024. El aumento porcentual fue del 8,9%, representando un valor de <strong>R$ 92,00.</strong>
                    </h6>
                  </h6>
                </li>
                <li>
                  <h3>Jornada de trabajo</h3>
                  <h6 style="color: #505050;">
                    La jornada laboral es el tiempo diario que el trabajador está disponible para su trabajo.

                    <br><br>Según la constitución federal, en el artículo 7, la jornada laboral estándar es de 8 horas diarias y 44 horas semanales, pero es posible que los trabajadores ajusten sus horarios y reduzcan el tiempo de trabajo, siempre que exista un acuerdo entre las partes, o un convenio colectivo entre empleados.

                    <br><br><h4>Descanso semanal</h4>
                    <h6 style="color: #505050;">
                    El descanso semanal es un período de 24 horas seguidas que normalmente se da los domingos, para todo tipo de trabajadores, ya sea en la ciudad, en el campo o en el trabajo doméstico.

                    Para aquellos que requieran trabajar los domingos, se establecerá un horario de rotación, organizado mensualmente y incluido en una tabla sujeta a inspección.

                    <br><br>Además del descanso semanal, el empleado deberá tomar un descanso mínimo de 1 hora para comer entre el turno de trabajo diario.
                    </h6>
                  </h6>
                </li>
