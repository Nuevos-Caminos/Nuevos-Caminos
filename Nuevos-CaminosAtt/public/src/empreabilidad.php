                <li>
                  <h3>Documentos Requeridos</h3><br>
                  <img src="imagesempre/cpf2-163d9a-1024.jpg" width="400" height="400">
                  <h6 style="color: #505050;"> <br>
                    El "Cadastro de Pessoa Física" (CPF) es el documento de registro nacional para los brasileños, el número de 11 dígitos es individual y es generado y mantenido por la Receita Federal. La única forma de modificar su CPF es bajo una decisión judicial.

                    <br><br>El CPF se utiliza para realizar un examen público, inscribirse en la universidad, abrir una cuenta bancaria, firmar una tarjeta de trabajo y se utiliza principalmente para la rendición de cuentas ante el Servicio de Ingresos Federales a través de la declaración de impuestos sobre la renta.

                    <br><br><h4 id="2">Lugares para hacer tu CPF:</h4>
                    <br><h6 style="color: #505050;">Cartório de Registro Civil de Pessoas Naturais;
                    <br><br>Caixa Econômica Federal;
                    <br><br>Correios.</h6>
                    <br><h4>Valor: R$7</h4>
                    </h6>
