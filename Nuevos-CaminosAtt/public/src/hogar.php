              <h3>Factura de electricidad</h3>
              <br><img src="imageshogar/contaluz.jpeg" height="500" width="700" id="imgbody">
              <h6 style="color: #505050;">
                <br>La tarifa eléctrica para consumidores residenciales se compone de los costes de generación, transmisión y distribución de electricidad, así como de impuestos y tasas.
                <br><br>La generación es el proceso de producción de electricidad, y representa una media del <strong id="5">31% de la factura eléctrica.</strong> La transmisión es el transporte de grandes bloques de energía a través del Sistema Interligado Nacional (SIN) y llena el <strong id="4">5% de la factura.</strong> La distribución, por su parte, es el servicio que lleva la electricidad desde las subestaciones hasta cada consumidor y supone el 21% de los costes.

                <br><br>El <strong>44% restante de la tarifa energética</strong> procede de los <strong>impuestos y tasas</strong> que pagan los consumidores de energía. Las tasas financian políticas públicas, como el descuento en la factura de la luz para consumidores con bajos ingresos y la producción de energía termoeléctrica en regiones remotas y aisladas, entre otras.
                <br><br>Por el lado de los impuestos, pagamos a la federación, los estados y los municipios, la mayor parte de los cuales corresponde al "Imposto sobre Circulação de Mercadorias e Serviços" (ICMS), un impuesto estatal que grava las facturas de electricidad.

                <br><br>En general, las facturas de energía incluyen información como los <strong>datos del cliente</strong>, el volumen de energía consumida en kilovatios hora (kWh), el detalle de la lectura del consumo, la <strong>fecha de vencimiento y el importe a pagar.</strong> Hay un espacio en el que se explican los conceptos que componen la tarifa, con el valor unitario y el total del concepto, las contribuciones PIS/Cofins, el ICMS, el valor de la tarifa, etc.
              </h6>
            </li>
