              <h3>Factura del agua</h3>
              <br><img src="imageshogar/contaagua.jpg" height="500" width="700" id="imgbody">
              <h6 style="color: #505050;" id="6">
                <br>En el cálculo de la factura del agua intervienen diferentes factores, por lo que la factura contiene mucha información, que puede ser más <strong>técnica o comercial.</strong> Entre la información comercial, que suele ser más sencilla y visible, están la <strong>fecha de vencimiento y el importe.</strong> Pero la factura también contiene otra información importante, como el consumo de cada cliente y las deudas existentes.

                <br><br>La información sobre el consumo, por ejemplo, muestra lo que ha pasado por el contador y se interpreta en la lectura del hidrómetro. Para calcular lo que se cobrará en la factura del agua, se tiene en cuenta la diferencia entre el volumen consumido el mes anterior, medido en metros cúbicos (m³), y el volumen consumido ese día, que representa el consumo del mes en curso.

                <br><br>El importe de la factura se determina restando estos resultados. De este modo, se sabe cuánto se ha consumido en ese periodo y luego se aplica la tarifa a los metros cúbicos consumidos. Para entender lo que esto significa, un consumo de 10 m³, por ejemplo, representa 10.000 litros de agua.

              </h6>
            </li>
            <li>
              <h3>IPTU</h3>
              <br><img src="imageshogar/iptu.jpg" height="500" width="700" id="imgbody">
              <h6 style="color: #505050;" id="7">
                <br>El cálculo del <strong>"Imposto sobre a Propriedade Predial e Territorial Urbana" (IPTU) </strong> se basa en el valor de mercado de la propiedad, que es una estimación del valor de mercado que tendría en una venta inmediata. <strong id="6">El ayuntamiento determina este valor</strong> basándose en criterios como la ubicación del inmueble, el tamaño de la parcela, la superficie de construcción y la calidad del acabado.

                <br><br>Después, el ayuntamiento define y aplica un tipo impositivo, que puede variar según el tipo y el uso de la propiedad. En otras palabras, el resultado de este cálculo es el importe que hay que pagar.

                <br><br>El IPTU es responsabilidad del propietario, ya sea un <strong>particular o una empresa.</strong> En algunos casos, el inquilino puede pagar el IPTU, siempre que esté claramente estipulado en el contrato.

                <br><br>No obstante, hay situaciones en las que el pago del impuesto puede estar exento. En general, están exentos los pensionistas, las personas discapacitadas y las propiedades con un valor inmobiliario bajo, pero las normas pueden variar según la legislación municipal.
              </h6>
              <br><br>
              <h4>Impago</h4>
              <h6 style="color: #505050;">
                <br>El impago del IPTU puede acarrear una serie de problemas al propietario. Además de las multas y los intereses de demora, la deuda puede inscribirse en la Lista de "Dívida Ativa" del municipio, lo que da lugar a una reclamación judicial. En casos extremos, <strong>la propiedad puede incluso ser subastada para saldar la deuda.</strong>
              </h6>
              <br><br>
